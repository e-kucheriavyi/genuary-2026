const LetterWidth = 8

func Write(screen *ebiten.Image, txt string, x, y, s float32, c color.Color) {
	for i, l := range txt {
		DrawLetter(
			screen,
			l,
			x+float32(i)*(LetterWidth*s),
			y,
			s,
			c,
		)
	}
}

func DrawLetter(screen *ebiten.Image, l rune, x, y, s float32, c color.Color) {
	m := GetLetterMap(l)
	utils.DrawBitmap(screen, m, x, y, s, LetterWidth, c)
}

